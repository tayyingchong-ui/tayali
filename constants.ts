function parseQuestions(): Question[] {
  const lines = questionsRawText.trim().split('\n');
  return lines.map((line, index) => {
    const id = index + 1;
    const match = line.match(/^\d+\.\s+(.*?)\s+A\.\s+(.*?)\s+B\.\s+(.*?)\s+C\.\s+(.*?)\s+D\.\s+(.*?)$/);
    if (!match) {
        // Fallback or error log
        return {
            id,
            text: line,
            options: ["A", "B", "C", "D"],
            answer: answerKeyRaw[id] || 'A'
        };
    }
    return {
      id,
      text: match[1],
      options: [match[2], match[3], match[4], match[5]],
      answer: answerKeyRaw[id]
    };
  });
}

